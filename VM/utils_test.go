package VM

import (
	"testing"

	"github.com/adamnite/go-adamnite/utils"
	"github.com/stretchr/testify/assert"
)

func Test_addressToInts(t *testing.T) {
	testAddress = []byte{0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8, 0x9, 0xa, 0xb, 0xc, 0xd, 0xe, 0xf, 0x10, 0x11, 0x12, 0x13}
	ad := utils.BytesToAddress(testAddress)

	foo := addressToInts(testAddress)
	assert.Equal(t, ad.Bytes(), uintsArrayToAddress(foo))

	assert.Equal(t, ad, utils.BytesToAddress(uintsArrayToAddress(foo)))
}
